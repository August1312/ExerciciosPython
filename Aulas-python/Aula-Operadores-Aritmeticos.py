# aula de operadores aritméticos(matematica)

adicao = (10 + 10) # Soma dois numeros ou mais ( + )

subtracao = (10 - 10) # Subtração subtrai dois numeros ou mais ( - )

multiplicacao = (10 * 10) # Multiplicação multiplica um ou mais numeros ( * ) 

divisao = (10 / 2.2) # Divisão de numeros quebrados exp: 1.1 ou 2.44 e utilizado somente uma barra ( / )

divisao_inteira = (10 // 2.2) # Divisão de numeros inteiros e utilizado duas barras ( // )

exponenciacao = (10 ** 2) # Exponenciação e utilizado dois simbolos de multiplicação ( ** ) obs:esponificação e quando o numero e mutiplicado por valor atribuido exp: 10**2= 10*10/ 10**5 = 10x5 vezes

modulo = (55 % 2) # Modulo para o calculo e usado a simbolo de porcentagem ( % ) obs: e utilizado para saber ser resta algum numero da divisao ou para saber ser o numero e impar ou par 

print('Adição:', adicao,)

print("Subtração:", subtracao)

print("Multiplicação:", multiplicacao)

print("Divisão:", divisao)

print("Divisão Inteira:", divisao_inteira)

print("Exponenciação:", exponenciacao)

print("Modulo:", modulo)


